class Consultation {
    constructor(id, created_at, data, horario, paciente, dentista, local, status, servico){
        this.id = id;
        this.created_at = created_at;
        this.data = data;
        this.horario = horario;
        this.paciente = paciente;
        this.dentista = dentista;
        this.local = local;
        this.status = status
        this.servico = servico
    }
}